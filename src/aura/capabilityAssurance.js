export function validateDigitalMoneyTwinEvent({
  before = {},
  after = {},
  explicitFees = 0,
  externalSettledMovements = 0,
  debits = [],
  credits = [],
  eventId,
  priorEventIds = []
} = {}) {
  const beforeTotal = sumValues(before)
  const afterTotal = sumValues(after)
  const feeTotal = Number(explicitFees)
  const settledTotal = Number(externalSettledMovements)
  const debitTotal = sumValues(debits)
  const creditTotal = sumValues(credits)
  const conservationDelta = Number((beforeTotal - (afterTotal + feeTotal + settledTotal)).toFixed(2))
  const issues = []

  if (Math.abs(conservationDelta) > 0.01) issues.push('value_conservation_failed')
  if (Math.abs(debitTotal - creditTotal) > 0.01) issues.push('debits_do_not_equal_credits')
  if (eventId && priorEventIds.includes(eventId)) issues.push('duplicate_event_would_duplicate_money')
  if (after.locked_funds !== undefined && after.locked_funds < 0) issues.push('locked_funds_negative')
  if (after.refund_queues !== undefined && after.refund_queues < 0) issues.push('refund_queue_negative')

  return {
    valid: issues.length === 0,
    issues,
    formula: VALUE_CONSERVATION_FORMULA,
    beforeTotal,
    afterTotal,
    explicitFees: feeTotal,
    externalSettledMovements: settledTotal,
    debitTotal,
    creditTotal,
    conservationDelta,
    releaseBlocked: issues.length > 0
  }
}

export function exploreStateSpace({
  states = [],
  transitions = [],
  terminalStates = ['released', 'refunded', 'partial_split'],
  evidenceRequiredTransitions = [],
  finalStates = ['released', 'refunded'],
  timeoutRules = {},
  retryRules = {},
  manualReviewRules = {},
  moneyHoldingStates = ['funded', 'pending_evidence', 'evidence_submitted', 'resubmission_window', 'human_review']
} = {}) {
  const stateSet = new Set(normalizeList(states))
  const terminalSet = new Set(normalizeList(terminalStates))
  const finalSet = new Set(normalizeList(finalStates))
  const outgoing = new Map()
  const incoming = new Map()
  const issues = []

  transitions.forEach((transition) => {
    const from = normalizeKey(transition.from)
    const to = normalizeKey(transition.to)
    if (!stateSet.has(from)) issues.push(`transition_from_unknown_state_${from}`)
    if (!stateSet.has(to)) issues.push(`transition_to_unknown_state_${to}`)
    outgoing.set(from, [...(outgoing.get(from) ?? []), transition])
    incoming.set(to, [...(incoming.get(to) ?? []), transition])
    if (transition.requiresEvidence && !transition.evidenceType) issues.push(`required_evidence_missing_type_${normalizeKey(transition.event ?? `${from}_to_${to}`)}`)
    if (finalSet.has(from) && !transition.allowFinalOverride) issues.push(`final_state_has_uncontrolled_exit_${from}`)
  })

  const deadEnds = [...stateSet].filter((state) => !terminalSet.has(state) && !(outgoing.get(state) ?? []).length)
  deadEnds.forEach((state) => issues.push(`dead_end_state_${state}`))
  normalizeList(moneyHoldingStates)
    .filter((state) => stateSet.has(state) && !timeoutRules[state] && !manualReviewRules[state])
    .forEach((state) => issues.push(`money_holding_state_without_timeout_or_review_${state}`))
  evidenceRequiredTransitions
    .map(normalizeKey)
    .forEach((event) => {
      const match = transitions.find((transition) => normalizeKey(transition.event) === event)
      if (!match) issues.push(`missing_evidence_transition_${event}`)
      if (match && !match.requiresEvidence) issues.push(`transition_lacks_required_evidence_${event}`)
    })
  Object.keys(retryRules).forEach((state) => {
    if (!stateSet.has(normalizeKey(state))) issues.push(`retry_rule_for_unknown_state_${normalizeKey(state)}`)
  })

  return {
    valid: issues.length === 0,
    issues,
    deadEnds,
    outgoing: Object.fromEntries([...outgoing.entries()].map(([state, paths]) => [state, paths.map((path) => normalizeKey(path.to))])),
    checks: STATE_SPACE_EXPLORER_CHECKS,
    releaseBlocked: issues.length > 0
  }
}

