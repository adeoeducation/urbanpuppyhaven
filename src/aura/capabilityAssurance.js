export function calculateCapabilityHealthScore({
  moneyIntegrity = 100,
  stateSafety = 100,
  fraudResistance = 100,
  uxClarity = 100,
  complianceConfidence = 100,
  runtimeReliability = 100,
  evidenceReliability = 100,
  runtimeIncidents = 0,
  manualReviewBacklog = 0
} = {}) {
  const breakdown = {
    money_integrity: Math.max(0, Math.min(100, Number(moneyIntegrity))),
    state_safety: Math.max(0, Math.min(100, Number(stateSafety))),
    fraud_resistance: Math.max(0, Math.min(100, Number(fraudResistance))),
    ux_clarity: Math.max(0, Math.min(100, Number(uxClarity))),
    compliance_confidence: Math.max(0, Math.min(100, Number(complianceConfidence))),
    runtime_reliability: Math.max(0, Math.min(100, Number(runtimeReliability))),
    evidence_reliability: Math.max(0, Math.min(100, Number(evidenceReliability)))
  }
  const baseScore = Object.values(breakdown).reduce((sum, score) => sum + score, 0) / Object.values(breakdown).length
  const incidentPenalty = Math.min(20, Number(runtimeIncidents) * 5)
  const backlogPenalty = Math.min(10, Math.floor(Number(manualReviewBacklog) / 10))
  const score = Math.max(0, Math.round(baseScore - incidentPenalty - backlogPenalty))

  return {
    score,
    breakdown,
    inputs: CAPABILITY_HEALTH_SCORE_INPUTS,
    improvementNeeded: Object.entries(breakdown)
      .filter(([, value]) => value < 80)
      .map(([key]) => key),
    state: score >= 90 ? 'healthy' : score >= 75 ? 'watch' : score >= 60 ? 'degraded' : 'restricted'
  }
}

