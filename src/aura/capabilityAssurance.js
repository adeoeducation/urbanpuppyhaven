export function detectCapabilityDrift({
  simulationExpectations = {},
  runtimeObserved = {},
  threshold = 0.05
} = {}) {
  const drift = Object.keys({ ...simulationExpectations, ...runtimeObserved }).flatMap((metric) => {
    const expected = Number(simulationExpectations[metric] ?? 0)
    const observed = Number(runtimeObserved[metric] ?? 0)
    return Math.abs(observed - expected) > Number(threshold)
      ? [{ metric: normalizeKey(metric), expected, observed, delta: Number((observed - expected).toFixed(4)) }]
      : []
  })
  const likelyCauses = new Set()
  drift.forEach(({ metric, observed }) => {
    if (metric.includes('dispute')) likelyCauses.add('bad_ux').add('fraud_attack')
    if (metric.includes('refund')) likelyCauses.add('economic_incentive_problem')
    if (metric.includes('jurisdiction')) likelyCauses.add('jurisdiction_mismatch')
    if (metric.includes('provider')) likelyCauses.add('provider_issue_stub')
    if (observed > 0.15) likelyCauses.add('wrong_assumptions')
  })

  return {
    drifted: drift.length > 0,
    drift,
    likelyCauses: [...likelyCauses],
    action: drift.length ? 'send_drift_case_to_capability_doctor_and_simulation_lab' : 'continue_monitoring'
  }
}

