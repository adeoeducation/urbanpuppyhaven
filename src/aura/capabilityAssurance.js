export function recommendReleaseMode({
  criticalInvariantsPassed = false,
  ledgerFailures = 0,
  missingRefundPath = false,
  policyApprovalPassed = false,
  testCoverage = 0,
  requiredTestCoverage = 95,
  simulationConfidence = 0,
  requiredSimulationConfidence = 90,
  watchtowerRulesInstalled = false,
  runtimePilotStable = false,
  disputeRate = 1,
  disputeThreshold = 0.05,
  criticalIncidents = 0,
  uxConfusion = 1,
  uxConfusionThreshold = 0.05,
  fraudRate = 1,
  fraudThreshold = 0.03
} = {}) {
  const controlledAllowed = criticalInvariantsPassed &&
    Number(ledgerFailures) === 0 &&
    !missingRefundPath &&
    policyApprovalPassed &&
    Number(testCoverage) >= Number(requiredTestCoverage) &&
    Number(simulationConfidence) >= Number(requiredSimulationConfidence) &&
    watchtowerRulesInstalled
  const generalAllowed = controlledAllowed &&
    runtimePilotStable &&
    Number(disputeRate) <= Number(disputeThreshold) &&
    Number(criticalIncidents) === 0 &&
    Number(uxConfusion) <= Number(uxConfusionThreshold) &&
    Number(fraudRate) <= Number(fraudThreshold)

  return {
    current_recommendation: generalAllowed ? 'general_release' : controlledAllowed ? 'controlled_release' : 'lab_only',
    controlledAllowed,
    generalAllowed,
    missingConditions: [
      !criticalInvariantsPassed && 'critical_invariants_passed',
      Number(ledgerFailures) > 0 && 'no_ledger_failures',
      missingRefundPath && 'no_missing_refund_path',
      !policyApprovalPassed && 'policy_approval_passed',
      Number(testCoverage) < Number(requiredTestCoverage) && 'test_coverage_threshold',
      Number(simulationConfidence) < Number(requiredSimulationConfidence) && 'simulation_confidence_threshold',
      !watchtowerRulesInstalled && 'watchtower_rules_installed'
    ].filter(Boolean)
  }
}

