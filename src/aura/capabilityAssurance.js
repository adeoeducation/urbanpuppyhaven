export function evaluateEconomicOutcome({
  platformExposure = 0,
  reserveAvailable = 0,
  expectedDefaultLoss = 0,
  feeRevenue = 0,
  operationalReviewCost = 0,
  insurancePool = 0,
  refundLiability = 0,
  chargebackRisk = 0,
  disputeRate = 0,
  proofFailureRate = 0,
  pendingHours = 0,
  breakEvenThreshold = 0
} = {}) {
  const totalRiskCost = Number(expectedDefaultLoss) + Number(operationalReviewCost) + Number(refundLiability) + Number(chargebackRisk)
  const reserveGap = Math.max(Number(platformExposure) + Number(expectedDefaultLoss) - Number(reserveAvailable) - Number(insurancePool), 0)
  const margin = Number(feeRevenue) - totalRiskCost
  const issues = []

  if (reserveGap > 0) issues.push('reserve_or_insurance_pool_insufficient')
  if (margin < Number(breakEvenThreshold)) issues.push('below_break_even_threshold')
  if (Number(disputeRate) >= 0.12) issues.push('dispute_rate_makes_manual_review_cost_risky')
  if (Number(proofFailureRate) >= 0.2) issues.push('proof_failure_rate_requires_release_limits')
  if (Number(pendingHours) > 48) issues.push('pending_funds_duration_can_reduce_trust')

  return {
    viable: issues.length === 0,
    issues,
    reserveGap,
    margin,
    totalRiskCost,
    checks: ECONOMIC_OUTCOME_CHECKS,
    releaseBlocked: reserveGap > 0 || margin < Number(breakEvenThreshold)
  }
}

