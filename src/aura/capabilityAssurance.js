export function validateSimulationDossier(dossier = {}) {
  const normalizedKeys = normalizeList(Object.keys(dossier))
  const missing = SIMULATION_DOSSIER_FIELDS.filter((field) => !normalizedKeys.includes(field))
  const issues = [...missing.map((field) => `missing_${field}`)]
  const coverageFields = [
    'persona_coverage',
    'jurisdiction_coverage',
    'money_flow_coverage',
    'state_transition_coverage',
    'fraud_path_coverage',
    'failure_mode_coverage'
  ]

  coverageFields.forEach((field) => {
    const value = Number(dossier[field] ?? dossier[field.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase())])
    if (!Number.isFinite(value) || value < 0.8) issues.push(`${field}_below_threshold`)
  })
  const confidence = Number(dossier.final_release_confidence_score ?? dossier.finalReleaseConfidenceScore ?? 0)
  if (!Number.isFinite(confidence) || confidence < 0.75) issues.push('final_release_confidence_below_threshold')
  if ((dossier.critical_risks_discovered ?? dossier.criticalRisksDiscovered ?? []).length) {
    issues.push('critical_risks_require_fix_before_release')
  }

  return {
    valid: issues.length === 0,
    missing,
    issues,
    fields: SIMULATION_DOSSIER_FIELDS,
    recommendedReleaseMode: issues.length ? 'controlled_or_blocked_until_fixes' : (dossier.recommended_release_mode ?? dossier.recommendedReleaseMode ?? 'guarded_live'),
    releaseBlocked: issues.length > 0
  }
}

