export function calculateWeightedCapabilityHealthScore(componentScores = {}) {
  const weights = CAPABILITY_HEALTH_SCORE_REQUIREMENTS.components
  const weightedTotal = Object.entries(weights).reduce((sum, [component, weight]) => {
    const score = Math.max(0, Math.min(100, Number(componentScores[component] ?? 0)))
    return sum + (score * weight)
  }, 0)
  const score = Math.round(weightedTotal / 100)
  const thresholds = CAPABILITY_HEALTH_SCORE_REQUIREMENTS.thresholds
  const status = score >= thresholds.healthy
    ? 'healthy'
    : score >= thresholds.watch
      ? 'watch'
      : score >= thresholds.degraded
        ? 'degraded'
        : score >= thresholds.restricted
          ? 'restricted'
          : 'suspend'

  return {
    score,
    status,
    drivesReleaseAndRuntimeDecisions: true
  }
}

