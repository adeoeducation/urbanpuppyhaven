export function validateCanonicalAssuranceContract(contract = {}) {
  const missingFields = CANONICAL_ASSURANCE_CONTRACT_SCHEMA.filter((field) => {
    const value = contract[field]
    if (Array.isArray(value)) return value.length === 0
    if (value && typeof value === 'object') return Object.keys(value).length === 0
    return value === undefined || value === null || value === ''
  })

  return {
    valid: missingFields.length === 0 && contract.contract_type === 'AURA_ASSURANCE_CONTRACT',
    missingFields,
    coreSchema: CANONICAL_ASSURANCE_CONTRACT_SCHEMA
  }
}

