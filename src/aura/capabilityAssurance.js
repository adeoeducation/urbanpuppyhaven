export function monitorRuntimeInvariants({
  releaseAttempted = false,
  evidenceApproved = false,
  refundAttempted = false,
  alreadyReleased = false,
  ledgerBalanced = true,
  holdExpired = false,
  holdResolved = true,
  transitionAllowed = true,
  amount = 0,
  kycLimit = Infinity,
  feeCharges = 1,
  unauthorizedAccess = false,
  complianceBreach = false,
  telemetryMissing = false,
  reviewDelayed = false
} = {}) {
  const violations = []
  if (releaseAttempted && !evidenceApproved) violations.push({ invariant: 'escrow_cannot_release_without_approved_evidence', severity: 'critical' })
  if (refundAttempted && alreadyReleased) violations.push({ invariant: 'refund_cannot_occur_after_release', severity: 'critical' })
  if (!ledgerBalanced) violations.push({ invariant: 'ledger_entry_must_balance', severity: 'critical' })
  if (holdExpired && !holdResolved) violations.push({ invariant: 'expired_hold_must_resolve', severity: 'high' })
  if (!transitionAllowed) violations.push({ invariant: 'state_transition_must_be_allowed', severity: 'critical' })
  if (Number(amount) > Number(kycLimit)) violations.push({ invariant: 'kyc_limit_must_not_be_exceeded', severity: 'critical' })
  if (Number(feeCharges) > 1) violations.push({ invariant: 'fee_cannot_be_charged_twice', severity: 'critical' })
  if (unauthorizedAccess) violations.push({ invariant: 'unauthorized_access_blocked', severity: 'critical' })
  if (complianceBreach) violations.push({ invariant: 'compliance_threshold_not_breached', severity: 'critical' })
  if (reviewDelayed) violations.push({ invariant: 'manual_review_sla_met', severity: 'medium' })
  if (telemetryMissing) violations.push({ invariant: 'runtime_telemetry_complete', severity: 'low' })

  const severityRank = ['low', 'medium', 'high', 'critical']
  const highestSeverity = violations.reduce(
    (highest, violation) => (severityRank.indexOf(violation.severity) > severityRank.indexOf(highest) ? violation.severity : highest),
    'low'
  )

  return {
    valid: violations.length === 0,
    violations,
    highestSeverity: violations.length ? highestSeverity : 'none',
    releaseBlocked: violations.some((violation) => ['high', 'critical'].includes(violation.severity))
  }
}

export function watchStateMachineRuntime({
  stateBefore = 'funded',
  stateAfter = 'pending_evidence',
  allowedTransitions = {
    funded: ['pending_evidence', 'refunded'],
    pending_evidence: ['evidence_submitted', 'expired'],
    evidence_submitted: ['evidence_approved', 'evidence_rejected'],
    evidence_approved: ['released'],
    expired: ['refunded'],
    released: [],
    refunded: []
  },
  hoursInState = 0,
  reviewSlaHours = 48,
  transitionHistory = [],
  terminalStates = ['released', 'refunded'],
  expectedTransitionMissing = false,
  frequency = 1,
  expectedFrequency = 10
} = {}) {
  const before = normalizeKey(stateBefore)
  const after = normalizeKey(stateAfter)
  const allowed = (allowedTransitions[before] ?? []).map(normalizeKey)
  const history = normalizeList(transitionHistory)
  const detections = []

  if (allowed.length && !allowed.includes(after)) detections.push('invalid_transition')
  if (expectedTransitionMissing) detections.push('missing_transition')
  if (history.length >= 4 && history.slice(-4).join('|') === history.slice(-2).concat(history.slice(-2)).join('|')) detections.push('transition_loop')
  if (Number(hoursInState) > Number(reviewSlaHours)) detections.push('state_stuck_too_long')
  if (terminalStates.map(normalizeKey).includes(before) && before !== after) detections.push('terminal_state_reopened')
  if (before.includes('review') && Number(hoursInState) > Number(reviewSlaHours)) detections.push('manual_review_backlog')
  if (Number(frequency) > Number(expectedFrequency)) detections.push('unexpected_transition_frequency')

  return {
    detections,
    stateBefore: before,
    stateAfter: after,
    escalationRequired: detections.length > 0,
    escalation: detections.includes('state_stuck_too_long') || detections.includes('manual_review_backlog')
      ? 'operator_escalation'
      : 'state_machine_repair_request'
  }
}

