function normalizeKey(value = '') {
  return String(value)
    .trim()
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_|_$/g, '')
}

function normalizeList(values = []) {
  return (Array.isArray(values) ? values : [values]).filter(Boolean).map(normalizeKey)
}

function hasAny(text = '', terms = []) {
  const normalized = normalizeKey(text)
  return terms.some((term) => normalized.includes(normalizeKey(term)))
}

function sumValues(values = {}) {
  if (Array.isArray(values)) {
    return values.reduce((sum, item) => sum + Number(item.amount ?? item.value ?? item ?? 0), 0)
  }
  return Object.values(values).reduce((sum, value) => sum + Number(value ?? 0), 0)
}

export function classifyAssuranceNeeds(request = '') {
  const text = String(request)
  const needs = new Set(['state_machine_simulation', 'invariant_tests', 'runtime_monitoring'])

  if (hasAny(text, ['send funds', 'send money', 'refund', 'wallet', 'payment', 'payout'])) {
    needs.add('ledger_simulation')
    needs.add('double_settlement_test')
    needs.add('stuck_funds_monitor')
  }
  if (hasAny(text, ['photo', 'gps', 'proof', 'location', 'evidence'])) {
    needs.add('evidence_failure_simulation')
    needs.add('proof_spoof_test')
    needs.add('evidence_mismatch_monitor')
  }
  if (hasAny(text, ['family', 'wife', 'child', 'mother', 'approval', 'emergency'])) {
    needs.add('permission_boundary_tests')
    needs.add('emergency_override_simulation')
    needs.add('approval_anomaly_monitor')
  }
  if (hasAny(text, ['susu', 'circle', 'default', 'insurance', 'trust score', 'fundability'])) {
    needs.add('multi_party_default_simulation')
    needs.add('fundability_stress_tests')
    needs.add('reserve_depletion_monitor')
  }

  return {
    request,
    needs: [...needs],
    assuranceSystems: AURA_CAPABILITY_ASSURANCE_SYSTEMS
  }
}

export function validateAssuranceContract(contract = {}) {
  const normalizedKeys = normalizeList(Object.keys(contract))
  const missing = UNIVERSAL_CAPABILITY_CONTRACT_ASSURANCE_FIELDS.filter((field) => !normalizedKeys.includes(field))
  const issues = [...missing.map((field) => `missing_${field}`)]

  if ((contract.money_objects ?? contract.moneyObjects)?.length && !(contract.ledger_rules ?? contract.ledgerRules)?.length) {
    issues.push('money_objects_without_ledger_rules')
  }
  if ((contract.states ?? []).length && !(contract.transitions ?? []).length) issues.push('states_without_transitions')
  if ((contract.verification_rules ?? contract.verificationRules)?.length && !(contract.monitoring_requirements ?? contract.monitoringRequirements)?.length) {
    issues.push('verification_rules_without_runtime_monitors')
  }
  if ((contract.policies ?? []).length && !(contract.consent_disclosure_rules ?? contract.consentDisclosureRules)?.length) {
    issues.push('policies_without_consent_disclosure')
  }

  return {
    valid: issues.length === 0,
    missing,
    issues,
    fields: UNIVERSAL_CAPABILITY_CONTRACT_ASSURANCE_FIELDS,
    releaseBlocked: issues.length > 0
  }
}

