export function evaluateRuntimeWatchtowerSignals(events = []) {
  const normalized = events.map(normalizeWatchtowerEvent)
  const detections = new Set()

  normalized.forEach((event) => {
    const payload = event.payload ?? {}
    if (event.type.includes('escrow') && normalizeKey(payload.status).includes('pending') && Number(payload.pending_hours ?? 0) > 72) detections.add('money_stuck')
    if (event.type.includes('duplicate') || payload.duplicate === true) detections.add('duplicate_settlement_attempts')
    if (event.type.includes('evidence_rejected') || payload.metadataMismatch) detections.add('evidence_anomalies')
    if (payload.locationSpoofRisk === true) detections.add('location_spoofing')
    if (payload.reserveCoverageBelowThreshold === true) detections.add('reserve_stress')
    if (payload.userConfusion === true) detections.add('ux_confusion')
    if (payload.policyViolation === true) detections.add('policy_violations')
  })
  const outputs = []
  if (detections.has('money_stuck')) outputs.push('incident_tickets', 'transaction_holds', 'new_regression_tests')
  if (detections.has('duplicate_settlement_attempts')) outputs.push('risk_flags', 'new_regression_tests')
  if (detections.has('evidence_anomalies') || detections.has('location_spoofing')) outputs.push('step_up_verification_requirements', 'operator_escalations')
  if (detections.has('reserve_stress') || detections.has('policy_violations')) outputs.push('capability_throttles', 'compliance_escalations')
  if (detections.has('ux_confusion')) outputs.push('capability_doctor_repair_requests', 'new_simulation_scenarios')

  return {
    normalizedEvents: normalized,
    detections: [...detections],
    outputs: [...new Set(outputs.length ? outputs : ['alerts'])],
    interventionRequired: detections.size > 0
  }
}

